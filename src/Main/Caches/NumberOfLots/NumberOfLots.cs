{
    public class LotNumbers
    {
        #region Properties
        private double numberOfLotsLeft;
        private double numberOfLotsRight;

        public double NumberOfLotsLeft
        {
            get { return numberOfLotsLeft; }
            set { numberOfLotsLeft = value; }
        }

        public double NumberOfLotsRight
        {
            get { return numberOfLotsRight; }
            set { numberOfLotsRight = value; }
        }
        #endregion
    }
}
